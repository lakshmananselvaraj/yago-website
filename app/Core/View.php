    public static function render(string $template, array $data = [], ?string $layout = 'main'): string
    {
        $meta = [];
        $content = self::renderTemplate($template, $data, $meta);

        if ($layout === null) {
            return $content;
        }

        return self::renderTemplate('layouts/' . $layout, array_merge($data, $meta, ['content' => $content]));
    }

    public static function partial(string $template, array $data = []): string
    {
        return self::renderTemplate($template, $data);
    }

    /**
     * Child templates set $pageTitle/$pageCss as plain local variables, but PHP's
     * include() scopes those to this call — they don't propagate to the layout's
     * separate renderTemplate() call on their own. Capture that small whitelist of
     * "view meta" locals here and let render() thread them into the layout's data.
     */
    private static function renderTemplate(string $template, array $data, ?array &$meta = null): string
    {
        $path = self::BASE_PATH . $template . '.php';

        if (!is_file($path)) {
            throw new \RuntimeException("View not found: {$template}");
        }

        extract($data, EXTR_SKIP);
        ob_start();
        include $path;
        $content = (string) ob_get_clean();

        if ($meta !== null) {
            foreach (['pageTitle', 'pageCss', 'hideFloatingThemeToggle', 'portal', 'active'] as $metaKey) {
                if (isset($$metaKey)) {
                    $meta[$metaKey] = $$metaKey;
                }
            }
        }

        return $content;
    }

    public static function e(?string $value): string
    {
        return htmlspecialchars($value ?? '', ENT_QUOTES, 'UTF-8');
    }

